package week2;

public class P2 {
    public static void main(String[] args) {

        byte b1 = 60, b2 = 70;
        int bSum = (b1 + b2);
        System.out.println("bSum (value): " + bSum);

        char c1 = 'A';
        char c2 = 1;
        int cSum = c1 + c2;
        System.out.println("cSum (value): " + cSum);
        System.out.println("cSum (letter): " + (char) cSum);
    }
}
